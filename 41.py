# вывести чило pi с заданной точностью

# ряд Лейбница:
# Pi/4 = 1/1 - 1/3 + 1/5 - 1/7 +.......

print('Введите значение n');
n = int(input ()) 

k = 1
pi = 0
while k <= 10000:
     a = 1/k
     b = 1/(k+2)
     pi = 4 * ((a - b)) + pi
     k = k + 4
print(round(pi,n))

